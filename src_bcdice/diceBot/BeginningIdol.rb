  def check_2D6(total_n, dice_n, signOfInequality, diff, dice_cnt, dice_max, n1, n_max)  # ゲーム別成功度判定(2D6)
    return '' unless( signOfInequality == ">=")
    
    if(dice_n <= 2)
      return " ＞ ファンブル(プロデューサーは変調を受け、PCは思い出1つ獲得)"
    elsif(dice_n >= 12)
      return " ＞ スペシャル！(PCは思い出1つ獲得)"
    elsif(total_n >= diff)
      return " ＞ 成功"
