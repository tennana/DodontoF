  def get_work_table_by_number(index, table )
    table.each do |item|
      number = item[0]
      if( number >= index )
        return getWorkTableValue( item[1] ), item[2]
      end
    end
  end

  def getWorkTableValue(data)
    if( data.kind_of?( Proc ) )
      return data.call()
    end
    
    return data
  end

  
